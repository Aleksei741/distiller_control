//******************************************************************************
//include
//******************************************************************************
#include "led.h"
#include "esp_log.h"          // ESP_LOGI
#include "driver/gpio.h"        // GPIO управление
//******************************************************************************
// Constants
//******************************************************************************
#define LED_GPIO 2
#define LED_ACTIVE_LOW 0   // LED светит при уровне 0
//******************************************************************************
// Type
//******************************************************************************

//******************************************************************************
// Variables
//******************************************************************************
//------------------------------------------------------------------------------
// Global
//------------------------------------------------------------------------------

//------------------------------------------------------------------------------
// Local Variable
//------------------------------------------------------------------------------
static int led_state = 0;  // 0 = OFF, 1 = ON
//------------------------------------------------------------------------------
// Local Class
//------------------------------------------------------------------------------

//******************************************************************************
// Function prototype
//******************************************************************************

//******************************************************************************
// Function
//******************************************************************************
void led_init(void)
{
    gpio_config_t io_conf = {
        .intr_type = GPIO_INTR_DISABLE,
        .mode = GPIO_MODE_OUTPUT,
        .pin_bit_mask = (1ULL << LED_GPIO),
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .pull_up_en = GPIO_PULLUP_DISABLE
    };

    gpio_config(&io_conf);

    led_state = 0;

    int level = LED_ACTIVE_LOW ? 1 : 0;
    gpio_set_level(LED_GPIO, level);
}
//------------------------------------------------------------------------------
int led_status(void)
{
    return led_state;
}
//------------------------------------------------------------------------------
void led_set(int on)
{
    led_state = on ? 1 : 0;
    int level = LED_ACTIVE_LOW ? !led_state : led_state;
    gpio_set_level(LED_GPIO, level);
}