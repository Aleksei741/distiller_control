/**
 * @brief Формирует запрос Modbus RTU на чтение discrete inputs (функция 0x02)
 * @param[in]  slave_id    Адрес устройства (1..247)
 * @param[in]  start_addr  Начальный адрес (0-based)
 * @param[in]  count       Количество inputs (1..2000)
 * @param[out] frame       Буфер для фрейма
 * @param[in]  frame_size  Размер буфера
 * @return Длина фрейма (в байтах) или 0 при ошибке
 */
size_t modbus_rtu_build_discrete_inputs( uint8_t slave_id, uint16_t start_addr, uint16_t count, uint8_t *frame, size_t frame_size);

/**
 * @brief Парсит ответ Modbus RTU на чтение discrete inputs
 * @param[in]  frame       Принятый фрейм
 * @param[in]  frame_len   Длина фрейма
 * @param[in]  slave_id    Ожидаемый slave ID
 * @param[in]  count       Ожидаемое количество inputs
 * @param[out] inputs      Буфер для inputs (1 байт = 8 inputs)
 * @return Код ошибки
 */
modbus_err_t modbus_rtu_parse_discrete_inputs( const uint8_t *frame, size_t frame_len, uint8_t slave_id, uint16_t count, uint8_t *inputs);
